#include <stdio.h>
#include "calc.h"
#include <stdlib.h>
/**
* op_add - adds two ints
* @a: the first int
* @b: the sec int
*
* Return: returns aan int
*/
int op_add(int a, int b)
{
	return (a + b);
}
/**
* op_sub - subtracts two ints
* @a: the first int
* @b: the sec int
*
* Return: returns aan int
*/
int op_sub(int a, int b)
{
	return (a - b);
}
/**
* op_mul - multiplies two ints
* @a: the first int
* @b: the sec int
*
* Return: returns aan int
*/
int op_mul(int a, int b)
{
	return (a * b);
}
/**
* op_div - divides two ints
* @a: the first int
* @b: the sec int
*
* Return: returns aan int
*/
int op_div(int a, int b)
{
	return (a / b);
}
/**
* op_mod - calculates the remainder
* @a: the first int
* @b: the sec int
*
* Return: returns aan int
*/
int op_mod(int a, int b)
{
	return (a % b);
}
